library(SeuratDisk)
library(Seurat)

dpa3_1 <- read.table('../outs/20231128_3dpa_23dpa_inDrops/NCBI.3dpa1.sc.counts.matrix', sep = '\t', header = T, row.names = 1)
make.names(rownames(dpa3_1)) -> rownames(dpa3_1)
dpa3_1_so <- CreateSeuratObject(counts = dpa3_1, min.cells = 0, min.genes = 0, project = "3dpa1")
saveRDS(dpa3_1_so, file = "../outs/20231128_3dpa_23dpa_inDrops/3dpa1.reform.rds")
SaveH5Seurat(dpa3_1_so, filename = "../outs/20231128_3dpa_23dpa_inDrops/3dpa1.reform.h5Seurat")
Convert("../outs/20231128_3dpa_23dpa_inDrops/3dpa1.reform.h5Seurat", dest = "h5ad")

